class NoticiaNYT {
	String web_url;
	String title;
	String lead_paragraph;
	String pub_date;

	public NoticiaNYT (String web_url, String title, String lead_paragraph, String pub_date){
		this.web_url = web_url;
		this.title = title;
		this.lead_paragraph = lead_paragraph;
		this.pub_date= pub_date;
	}
}